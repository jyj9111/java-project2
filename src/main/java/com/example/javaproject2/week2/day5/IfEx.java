package com.example.javaproject2.week2.day5;

public class IfEx {
    public static void main(String[] args) {

        if(true){ //조건이 true 이면 실행
            System.out.println("항상 실행 됩니다.");
        }
        if(false) { // 조건이 false 이면 실행되지 않음
            System.out.println("항상 실행 되지 않습니다.");
        }
    }
}
